#include <stdio.h>

int max(int a , int b )
{
    if(a>b){
        return a;
    }else{
        return b;
    }
}

int main()
{
    int a1 = 26;
    int a2 = 23;
    int maxnum = max(a1,a2);
    printf("The maxnum is %d \n",maxnum);
    return 0;
}
